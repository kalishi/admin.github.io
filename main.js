$(function () {
  function parseMenu(ul, menu) {
    for (var i = 0; i < menu.length; i++) {
      var li = $(ul).append(
        '<li class="font-medium text-blue-600 dark:text-blue-500">' + menu[i].name + '</li>');
      if (menu[i].sub != null) {
        var subul = $('<ul class="list px-8  mt-2 space-y-1 list-decimal  list-inside"></ul>');
        $(li).append(subul);
        parseMenu($(subul), menu[i].sub);
      }
    }
  }

  var menu_ouest = $('#menu-ouest');
  var menu_sudest = $('#menu-sudest');
  var menu_nord = $('#menu-nord');
  var menu_nordest = $('#menu-nordest');
  var menu_artibonite = $('#menu-artibonite');
  var menu_centre = $('#menu-centre');
  var menu_sud = $('#menu-sud');
  var menu_grandeanse = $('#menu-grandeanse');
  var menu_nordouest = $('#menu-nordouest');
  var menu_nippes = $('#menu-nippes');

  parseMenu(menu_ouest, JSON_OUEST.menu);
  parseMenu(menu_sudest, JSON_SUDEST.menu);
  parseMenu(menu_nord, JSON_NORD.menu);
  parseMenu(menu_nordest, JSON_NORDEST.menu);
  parseMenu(menu_artibonite, JSON_ARTIBONITE.menu);
  parseMenu(menu_centre, JSON_CENTRE.menu);
  parseMenu(menu_sud, JSON_SUD.menu);
  parseMenu(menu_grandeanse, JSON_GRANDEANSE.menu);
  parseMenu(menu_nordouest, JSON_NORDOUEST.menu);
  parseMenu(menu_nippes, JSON_NIPPES.menu);
});


